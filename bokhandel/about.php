<h2 class="wp-block-heading">1980-talet&nbsp;&nbsp;</h2>



<p>Under 1980-talet öppnade Akademen butiker i Åbo, Tammerfors och Jyväskylä. År 1989 öppnades även Cafe Alto i&nbsp;Bokhuset i utrymmet där&nbsp;Akademens telefonväxel&nbsp;tidigare huserade.&nbsp;Alvar Aaltos designade möbler hade Stockmann räddat året innan då Café Columbia stängde sina dörrar i Järnhuset.&nbsp;Läs mera om Cafe Altos historia <a href="https://www.akateeminen.com/sv/cafe-aalto/" data-type="page" data-id="229">här</a>.&nbsp;&nbsp; <br><br>1989&nbsp;ordnades Bokens natt, ett evenemang som därefter blivit en klassiker på Akademen. Konceptet blev en omedelbar succé och har ordnats i samband med Konstens natt sedan dess.&nbsp;&nbsp;</p>



<h2 class="wp-block-heading">1990-talet&nbsp;– Akademens nätförsäljning inleddes</h2>



<p>Under 1990-talet inleddes&nbsp;nätförsäljning. År 1994&nbsp;startade Akademen en&nbsp;beställningstjänst för företagskunder på internet. Akademen öppnade sin första egna hemsida år 1997 och två år senare öppnades även en webbshop för privatkunder.&nbsp;&nbsp;</p>



<h2 class="wp-block-heading">2000-talet</h2>



<p>År 2015 såldes Akademiska Bokhandeln till mediebolaget Bonnier. Akademen startade ett samarbete med Gros Bokhandel i Vasa år 2020.</p>



<p>Under 2000-talet har Akademen haft flera anledningar till att ordna exceptionella fester. Finlands äldsta bokhandel, Akademiska Bokhandeln, firade sin 125-årsdag den 18.7.2018.&nbsp;Träffpunkten som fyllde 50 år 2020 vittnar om en lång tradition av att utgöra en röst i samhället.&nbsp;Akademen är fortfarande den mötesplats och inspirationskälla som den varit för kulturvänner i snart 130 år.&nbsp;</p>

