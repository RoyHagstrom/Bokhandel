											<h1 class="aka-display aka-title title mb-4">Historia</h1>
										
<p class="has-primary-1-color has-text-color">Akademiska Bokhandeln blev en del av Bonnier Books i oktober 2015 och har sedan dess fortsatt att fungera självständigt som en inspirerande mötesplats för alla bokälskare. Vi är engagerade i läsning och livslångt lärande. Även vår långa historia säger det – böcker har alltid haft en speciell plats i våra hjärtan. <br><br>Akademens webbplats har uppdaterats flera gånger under åren, senast i oktober 2021. Akademens logo och visuella utseende förnyades i samband med företagets brandförnyelse. Vår förhoppning med&nbsp;förnyelsen&nbsp;är att&nbsp;Akademen ska&nbsp;kunna betjäna sina kunder och samarbetspartners ännu bättre.</p>



<h2 class="wp-block-heading">1890-1930-talet&nbsp;</h2>



<p>Akademiska Bokhandeln grundades sommaren 1893, och hade från första början som målsättning att kunna erbjuda sina kunder ett brett urval litteratur på både finska och svenska, och dessutom även utländska titlar, vilket var en sällsynthet i Helsingfors på den tiden. För Akademens grundare Alvar Renqvist och Gösta Branders var bokhandeln inte bara ett företag, utan även ett bidrag för folkbildningen.</p>



                    
                


    
