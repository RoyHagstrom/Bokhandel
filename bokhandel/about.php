<p>Bland kulturvänner i Helsingfors väckte sammanslagningen med Stockmann i början både tvivel och missnöje, många ifrågasatte hur varuhuset skulle kunna gynna bokhandelns verksamhet. Genom att tydligt avgränsa bokhandelns utrymmen från resten av varuhuset kom man fram till en kompromiss som många var nöjda med – och dessutom hittade nya kunder till Akademen, inte minst eftersom tidskriftssortimentet kunde utökas rejält.</p>



<h2 class="is-style-default wp-block-heading">1940-talet: Akademen under kriget&nbsp;</h2>



<p>Under andra världskriget hörde böcker till de varor som inte ransonerades, vilket gjorde att böcker sålde bra, och Akademens försäljning under åren 1940 – 1944 räknas ha gått upp med 190 %. Inhemska författare så som&nbsp;Mika Waltari, Erkki&nbsp;Palolampi, Toivo&nbsp;Pekkanen,&nbsp;Iiris&nbsp;Uurto&nbsp;och Martti&nbsp;Merenmaa&nbsp;var populära, och lästes flitigt såväl på hemmaplan som ute vid fronten.&nbsp;&nbsp;</p>



                    
                


    
